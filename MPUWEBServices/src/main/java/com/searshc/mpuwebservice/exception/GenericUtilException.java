package com.searshc.mpuwebservice.exception;

public class GenericUtilException extends Exception {


	/**
	 * 
	 */
	private static final long serialVersionUID = 8785234908879L;

	public GenericUtilException() {
		super();
	}

	public GenericUtilException(String message, Throwable cause) {
		super(message, cause);
	}

	public GenericUtilException(String message) {
		super(message);
	}

	public GenericUtilException(Throwable cause) {
		super(cause);
	}
	
}
