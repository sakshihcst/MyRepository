package com.searshc.mpuwebservice.vo;

import java.sql.Date;

public class POG {
	
	private int busFillQty;
	private String description;
	private int divNbr;
	private Date planEndDate;
	private int planGroupNbr;
	private Date planStartDate;
	private String seqNbr;

	private String spcPlanNbr;

	public int getBusFillQty() {
		return busFillQty;
	}

	public String getDescription() {
		return description;
	}

	public int getDivNbr() {
		return divNbr;
	}

	public Date getPlanEndDate() {
		return planEndDate;
	}

	public int getPlanGroupNbr() {
		return planGroupNbr;
	}

	public Date getPlanStartDate() {
		return planStartDate;
	}

	public String getSeqNbr() {
		return seqNbr;
	}

	public String getSpcPlanNbr() {
		return spcPlanNbr;
	}

	public void setBusFillQty(int busFillQty) {
		this.busFillQty = busFillQty;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public void setDivNbr(int dvsnnbr) {
		this.divNbr = dvsnnbr;
	}

	public void setPlanEndDate(Date planChkToDt) {
		this.planEndDate = planChkToDt;
	}

	public void setPlanGroupNbr(int planGroupNbr) {
		this.planGroupNbr = planGroupNbr;
	}

	public void setPlanStartDate(Date planChkFromDt) {
		this.planStartDate = planChkFromDt;
	}

	public void setSeqNbr(String seqNbr) {
		this.seqNbr = seqNbr;
	}

	public void setSpcPlanNbr(String spcPlanNbr) {
		this.spcPlanNbr = spcPlanNbr;
	}

	@Override
	public String toString() {

		StringBuffer buffer = new StringBuffer();
		buffer.append("spcPlanNbr : ");
		buffer.append(spcPlanNbr);
		buffer.append("\n");
		buffer.append("busFillQty : ");
		buffer.append(busFillQty);
		buffer.append("\n");
		buffer.append("planChkFromDt : ");
		buffer.append(planStartDate);
		buffer.append("\n");
		buffer.append("planChkToDt : ");
		buffer.append(planEndDate);
		buffer.append("\n");
		buffer.append("dvsnNbr : " );
		buffer.append(divNbr);
		buffer.append("\n");
		buffer.append("itemSeqNbr : ");
		buffer.append(seqNbr);
		buffer.append("\n");

		return buffer.toString();
	}

}
